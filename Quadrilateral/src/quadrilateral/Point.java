package quadrilateral;

public class Point {
    private double x;
    private double y;
    
public void init(double xVal, double yVal){//Initialization
        this.x = xVal;
        this.y = yVal;
    }

public double getX (){
  return this.x;
 }//End getPoint

public double getY (){
  return this.y;
 }//End getPoint

}//End class point